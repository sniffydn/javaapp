    public void drawLower(int x, int y, GraphicsMap g2d)
    {
        g2d.setColorSpray(colorSprayHue, colorSpraySaturation, colorSprayBrightness, colorSprayType, colorSprayFrequency);
        paintTL(x,0,g2d);

        if(prevY == -1)
        {
            paintTL3(x,0,g2d);
            if(y != -1)
                switcher = !switcher;
        }
        else
        {
            if(switcher)
                paintTL1(x,0,g2d);
            else
                paintTL2(x,0,g2d);

        }


        prevY = y;

        if(y == -1)
            y = 0;
        paintB1(x,y,g2d);
        paintB(x,0,g2d);
    }
    int prevY = 0;



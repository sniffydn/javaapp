    private void changeColor() {
        try {
            if (jTextField1.getText().indexOf(",") != -1) {
                StringTokenizer tok = new StringTokenizer(jTextField1.getText(), ",");
                tok.nextToken();
                int red = Integer.valueOf(tok.nextToken()).intValue();
                int green = Integer.valueOf(tok.nextToken()).intValue();
                int blue = Integer.valueOf(tok.nextToken()).intValue();

                jColorChooser1.setColor(red, green, blue);
            }
        } catch (Exception e) {

        }

    }

    private void resetPanels() {
//        for (int i = 0; i < colors.getLength(); i++) {
//            panels[i].setBackground(colors.get(i).getColor());
//        }
        colorArrayPanel.setVisible(false);
        colorArrayPanel.removeAll();
        javax.swing.JPanel temp1 = new javax.swing.JPanel();
        temp1.setLayout(new java.awt.GridLayout(0, 1));
        for (int i = 0; i < colors.getLength(); i++) {
            ColorPanel cp = new ColorPanel(colors.getColorsAt(i), i);
            cp.addMouseListener(new java.awt.event.MouseAdapter() {

                public void mouseReleased(java.awt.event.MouseEvent evt) {
                    formMouseReleased(evt);
                }
            });
            temp1.add(cp);
        }
        colorArrayPanel.add(temp1);

        for (int index = 0; index < colors.getSize(); index++) {
            javax.swing.JPanel temp = new javax.swing.JPanel();
            temp.setLayout(new java.awt.GridLayout(0, 1));
            for (int i = 0; i < colors.getLength(); i++) {
                ColorPanel cp = new ColorPanel(colors.getColorAt(index, i));
                cp.addMouseListener(new java.awt.event.MouseAdapter() {

                    public void mouseReleased(java.awt.event.MouseEvent evt) {
                        formMouseReleased(evt);
                    }
                });
                temp.add(cp);
            }

            colorArrayPanel.add(temp);
        }
        colorArrayPanel.setVisible(true);
    }

